package assignments;

/*Write a Java program to test a number is positive or negative.

Test Data

Input number: 35 -- positive number

Input number: -11 -- negative number
 
 * */
 
public class PositiveOrNegative {

	public static void main(String[] args) {
		int a =-11;
				
		if(a>=0) {
			System.out.println("Entered number is positive: " +a);
		}
		else {
			System.out.println("Entered number is negative: " +a);
		}

	}

}
