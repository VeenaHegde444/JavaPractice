public class FindGreatestNumber {

	public static void main(String[] args) {
		int i=10,j=45,k=30;
		
		if(i>j && i>k) {
			System.out.println(i+" is greatest");
		}
		else if(j>i && j>k) {
			System.out.println(j+" is greatest");
		}
		else if(k>i && k>j){
			System.out.println(k+" is greatest");
		}
		
			
			
		
	}

}
