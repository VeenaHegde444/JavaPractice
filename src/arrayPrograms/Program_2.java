package arrayPrograms;
//What is an anonymous array? Give example?
public class Program_2 {

	public static void main(String[] args) {
		System.out.println(new int[] {1,2,3,4,5}.length);
		System.out.println(new int[] {10,20,30,40,50}[3]);
	}

}

//Ans: Anonymous array is an array without reference.